package Day2_PrintandVariables;

public class Topic4_EscapeSequence {
    public static void main(String[] args) {
        // \t (tab space or paragraph space) it is going to leave paragraph space on the console.
        System.out.println("Thi sis the line and If I want to prag space\trandomthings");


        // \n It is going to move on the new line on the console

        System.out.println("this is a code where I go to next \nline");

        // \" prints double quote " on the console
        System.out.println("This is \"Zeliha\"");

        // \' prints one quote ' on the console
        System.out.println("This is \'computer is a device\'");

        System.out.println("\"\\ This is Fahrettin\\\"");

        //  \t:paragraph space
        //  \n: new line
        //  \": prints double quote " on the console
        //  \\: prints \ on the console
        //  \': prints single quote
        //  ' : prints single quote

        // I want to print \Nureddin\
        System.out.println("\\Nureddin\\");
    }
}
